<template>
  <div class="login-container">
    <img src="@/assets/images/bg.jpg" class="login-b" alt="" />
    <vue-particles
      color="#dedede"
      :particleOpacity="0.9"
      :particlesNumber="80"
      shapeType="circle"
      :particleSize="4"
      linesColor="#dedede"
